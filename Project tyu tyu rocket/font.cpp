//****************************************************************************************************************
#include"font.h"
#include "player.h"
#include"direct3d.h"
#include"sprite.h"
#include"error.h"

//****************************************************************************************************************
// メンバ変数の実体を宣言
//****************************************************************************************************************
SpriteFont* Font::font_;

//****************************************************************************************************************
// 初期化
//****************************************************************************************************************
bool Font::init(){
    font_ = new SpriteFont( Direct3D::getDevice(), L"font.spritefont" );
    // エラーチェック
    if( !font_ ){
        Error::showDialog( L"スプライトフォントのメモリ確保" );
        return false;
    }
    return true;
}

//****************************************************************************************************************
// 破棄
//****************************************************************************************************************
void Font::destroy(){
    delete[] font_;
}

//****************************************************************************************************************
// 描画
//****************************************************************************************************************
void Font::draw( const wchar_t* String, const Vector2& Position ){
    font_->DrawString( Sprite::getSprite(), String, Position );
}
